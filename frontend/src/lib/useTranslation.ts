export function useTranslation() {
    const { user } = useAuth();
    const language = user?.settings?.language || 'English';

    const t = (key: keyof typeof globalTranslations.English): string => {
        const dict = globalTranslations[language as keyof typeof globalTranslations] || globalTranslations.English;
        return dict[key] || globalTranslations.English[key];
    };

    return { t, language };
}
